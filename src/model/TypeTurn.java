package model;

public class TypeTurn {
	

	
	private String name;
	private float time;
	
	/**
	 * @param name
	 * @param time
	 */
	
	public TypeTurn(String name, float time) {
	
		this.name = name;
		this.time = time;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the time
	 */
	public float getTime() {
		return time;
	}

	
	
	
	
	
	
}
